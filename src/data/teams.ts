import type { Coach, CoachStyle, Game, Player, Role, TeamSeason } from '../types';

// Atributos: aim / clutch / consistency / awp / igl - escala 50-99.
// Curadoria baseada em Liquipedia (elencos/eras) e historico HLTV (top20, MVPs).

function P(
  nick: string,
  name: string,
  country: string,
  role: Role,
  aim: number,
  clutch: number,
  consistency: number,
  awp = 58,
  igl = 55,
): Omit<Player, 'id'> {
  return { nick, name, country, role, aim, clutch, consistency, awp, igl } as Omit<Player, 'id'>;
}

function C(nick: string, name: string, country: string, rating: number, style: CoachStyle): Coach {
  return { nick, name, country, rating, style };
}

function T(
  id: string,
  team: string,
  tag: string,
  era: string,
  game: Game,
  country: string,
  teamwork: number,
  honors: string,
  colors: [string, string],
  mapPrefs: Record<string, number>,
  coach: Coach,
  players: Omit<Player, 'id'>[],
): TeamSeason {
  return {
    id,
    team,
    tag,
    era,
    game,
    country,
    teamwork,
    honors,
    colors,
    mapPrefs,
    coach,
    players: players.map((p) => ({ ...p, id: `${id}__${p.nick.toLowerCase().replace(/[^a-z0-9]/g, '')}` })),
  };
}

